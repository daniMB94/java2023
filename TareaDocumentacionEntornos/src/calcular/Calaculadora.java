public class Calaculadora {

	/**
	 * <h2>Método para comprobar si un número es par</h2>
	 * @param a
	 * @return
	 */
	public boolean esPar(int a) {
		return a % 2 == 0;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int suma(int a, int b){
		return a+ b;
	}
}
